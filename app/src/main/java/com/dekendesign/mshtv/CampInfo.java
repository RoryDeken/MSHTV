package com.dekendesign.mshtv;

public class CampInfo {
    private String campImage;
    private String campTitle;
    private String campBodyText;

    public String getCampImage() {
        return campImage;
    }

    public void setCampImage(String campImage) {
        this.campImage = campImage;
    }

    public String getCampTitle() {
        return campTitle;
    }

    public void setCampTitle(String campTitle) {
        this.campTitle = campTitle;
    }

    public String getCampBodyText() {
        return campBodyText;
    }

    public void setCampBodyText(String campBodyText) {
        this.campBodyText = campBodyText;
    }
}
